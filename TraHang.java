public class TraHang {
    private String tongCP;
    private String maHD;
    private String ngayTao;
    private String ID;
    private String ngayCN;
    private String lyDo;

    public TraHang() {
    }

    public TraHang(String tongCP, String maHD, String ngayTao, String ID, String ngayCN, String lyDo) {
        this.tongCP = tongCP;
        this.maHD = maHD;
        this.ngayTao = ngayTao;
        this.ID = ID;
        this.ngayCN = ngayCN;
        this.lyDo = lyDo;
    }

    public String getTongCP() {
        return tongCP;
    }

    public void setTongCP(String tongCP) {
        this.tongCP = tongCP;
    }

    public String getMaHD() {
        return maHD;
    }

    public void setMaHD(String maHD) {
        this.maHD = maHD;
    }

    public String getNgayTao() {
        return ngayTao;
    }

    public void setNgayTao(String ngayTao) {
        this.ngayTao = ngayTao;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getNgayCN() {
        return ngayCN;
    }

    public void setNgayCN(String ngayCN) {
        this.ngayCN = ngayCN;
    }

    public String getLyDo() {
        return lyDo;
    }

    public void setLyDo(String lyDo) {
        this.lyDo = lyDo;
    }
   

    

   
   
    
    
}
